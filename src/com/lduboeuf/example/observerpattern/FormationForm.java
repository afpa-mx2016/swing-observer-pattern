    List<FormationEventListener> listeners = new ArrayList<>();
    
    
    //on déclare la liste des évènements dans une interface ( peut être dans une classe externe)
    public interface FormationEventListener {
        public void onCreatedFormation(Formation newForm);
        public void onUpdatedFormation(Formation updatedForm);
    }
    
