    /**
     * abonnement aux évènements de ce composant
     * @param listener 
     */
    public void addEventListener(FormationEventListener listener){
        this.listeners.add(listener);
    }
    
    /**
     * on notify les écouteurs de l'évènement d'une nouvelle formation
     * @param newForm 
     */
    private void fireCreateFormation(Formation newForm){
        for (FormationEventListener listener: this.listeners){
            listener.onCreatedFormation(newForm);
        }
    }

